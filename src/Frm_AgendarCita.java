    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
                                               
          String areaMedica = jComboBoxArea.getSelectedItem().toString();
        String doctor = jComboBoxDoctor.getSelectedItem().toString();
        String tipoSangre = jComboBoxSangre.getSelectedItem().toString();
        String fecha = jDateChooserFecha.getDateFormatString().toString();
        String horario = jComboBoxHorario.getSelectedItem().toString();

        // Obtener la fecha y formatearla como una cadena en formato "dd/MM/yyyy"
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String fechaFormateada = dateFormat.format(jDateChooserFecha.getDate());

        tablaCita.cargarModelo(); // Llamar al método cargarModelo() en la instancia de Frm_TablaCita

        GestorCitas.guardarCita(areaMedica, doctor, tipoSangre, fechaFormateada, horario);

        JOptionPane.showMessageDialog(this, "Cita guardada exitosamente.");

        // Restablecer los campos de selección a su estado inicial
        jComboBoxArea.setSelectedIndex(0);
        jComboBoxDoctor.setSelectedIndex(0);
        jComboBoxSangre.setSelectedIndex(0);
        jDateChooserFecha.setDate(null);
        jComboBoxHorario.setSelectedIndex(0);   
                                     
   
    }//GEN-LAST:event_btnGuardarActionPerformed

