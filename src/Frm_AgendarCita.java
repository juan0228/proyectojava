       public static void guardarCita(String areaMedica, String doctor, String tipoSangre, String fecha, String horario) {
        String datosCita = areaMedica + "," + doctor + "," + tipoSangre + "," + fecha + "," + horario;
        try {
            String rutaArchivo = "C:/ruta/completa/hasta/AgendaCitas.txt"; // Reemplaza con la ruta real en tu sistema

            FileWriter archivo = new FileWriter("AgendaCitas.txt", true);
            BufferedWriter escritura = new BufferedWriter(archivo);
            escritura.write(datosCita);
            escritura.newLine();
            escritura.close();
        } catch (IOException e) {
            System.out.println(e);
        }
    }

